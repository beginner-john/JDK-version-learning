package LeanJDK9;

public class Features3 {


    /**
     *      4、接口的私有方法
     *      在JDK8中接口运行使用静态方法和默认方法后，JDK9可以在接口中使用私有方法。
     *
     *
     *      5、改进try-with-resources
     *      JDK8中新增了try-with-resources语句，可以自动关闭需要关闭的资源文件。但是必须在try语句后的括号中初始化需要关闭的资源。
     *      在JDK9中改进了try-with-resources语句，你可以在try外初始化资源，然后在try后的括号中添加需要自动关的资源即可。
     *
     *
     *      6、改进钻石操作符
     *      JDK9中钻石操作符可以使用匿名实现类，可以在匿名实现类中重写方法等操作。
     *
     */
}
